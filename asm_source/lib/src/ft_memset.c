#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*array;

	if (len == 0 || len + 1 <= 0 || len + 1 < len)
		return (b);
	array = (unsigned char *)b;
	while (len != 0)
	{
		*array++ = (unsigned char)c;
		len--;
	}
	return ((void *)b);
}
